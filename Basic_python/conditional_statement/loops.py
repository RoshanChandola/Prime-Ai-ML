# loops are use to execute a block of code repeatedly as long as a certain condition is met. Here are some examples of loops in Python: 
#  for loop: Used to iterate over a sequence (like a list, tuple, or string) or other iterable objects.   
#  while loop: Repeats a block of code as long as a specified condition is true.
# Example of for loop:  
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)
